import Foundation
import SwiftUI

extension Font {
    public static func Akaya(size: CGFloat) -> Font {
        return Font.custom("AkayaTelivigala-Regular", size: size)
    }
    public static func Cedarville(size: CGFloat) -> Font {
        return Font.custom("Cedarville Cursive", size: size)
    }

}
